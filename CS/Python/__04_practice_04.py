if x > y:
    x,y = y,x
for factor in range(x,0,-1):
    if x % factor == 0 and  y % factor == 0:
        print('%d 和 %d 的最大公约数是%d' % (x , y,factor))
        print('%d 和 %d 的最大公倍数是%d' % (x , y, x * y // factor))
